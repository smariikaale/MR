#git-a VCS(Version Control System) that tracks our codebase
#github-online platform that stores our projects

#Git Flow:
#git init -initializes the git(one time for a project)
#git add file_name-adds the content to git //git add .-adds all untracted files
#commit changes-git commit ~m "message"
#push content to githuh-git push ~u origin branch_name=>~u is used in first push
#~u-upstream=>tracks branch_name for next push
#git push origin branch_name//to find what's in ~u
#U-untracted
#A-added to git
#M-modified
#Connecting project to github repository- git remote add origin ssh_key(one time for a project)